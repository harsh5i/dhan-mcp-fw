"""
Strategy schema — defines what a strategy looks like and validates it.
Strategies are YAML files generated by the AI during discussion with user.
"""

import yaml
import os
import uuid
from datetime import datetime
from pathlib import Path


STRATEGIES_DIR = os.path.expanduser("~/.dhan-mcp/strategies")


